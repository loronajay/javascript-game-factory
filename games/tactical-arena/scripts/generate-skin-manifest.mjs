import { readdir, writeFile } from "node:fs/promises";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

import { UNIT_TYPES } from "../src/core/unitCatalog.js";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SKIN_ROOT = join(ROOT, "assets", "units", "skins");
const OUT_FILE = join(ROOT, "src", "ui", "skinManifest.generated.js");

function inferSlug(type, file) {
  const basename = file.replace(/\.png$/i, "");
  const suffix = `-${type}`;
  return basename.endsWith(suffix) ? basename.slice(0, -suffix.length) : basename;
}

function sortEntries(left, right) {
  const typeOrder = Object.keys(UNIT_TYPES);
  const typeDelta = typeOrder.indexOf(left.type) - typeOrder.indexOf(right.type);
  if (typeDelta !== 0) return typeDelta;
  if (left.slug === "summer-vibes" && right.slug !== "summer-vibes") return -1;
  if (right.slug === "summer-vibes" && left.slug !== "summer-vibes") return 1;
  return left.slug.localeCompare(right.slug) || left.file.localeCompare(right.file);
}

const entries = [];
for (const type of Object.keys(UNIT_TYPES)) {
  const unitDir = join(SKIN_ROOT, type);
  let files = [];
  try {
    files = await readdir(unitDir);
  } catch (error) {
    if (error?.code !== "ENOENT") throw error;
  }
  for (const file of files) {
    if (!file.toLowerCase().endsWith(".png")) continue;
    entries.push({ type, slug: inferSlug(type, file), file });
  }
}

entries.sort(sortEntries);

const body = `// Generated by scripts/generate-skin-manifest.mjs. Do not edit by hand.
// Add PNGs to assets/units/skins/<unit>/, then rerun the generator.
export const SKIN_MANIFEST = Object.freeze(${JSON.stringify(entries, null, 2)
  .replace(/"([^"]+)":/g, "$1:")
  .replace(/\n/g, "\n")});
`;

await writeFile(OUT_FILE, body, "utf8");
console.log(`Wrote ${relative(ROOT, OUT_FILE)} with ${entries.length} skins.`);
